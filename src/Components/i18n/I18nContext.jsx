function resolveInitialLang() {
	if (typeof window === "undefined") {
		return "ar";
	}
	const saved = window.localStorage.getItem("lang");
	if (saved === "ar" || saved === "en") {
		return saved;
	}
	const navLang = typeof navigator !== "undefined" ? navigator.language : "ar";
	return navLang?.toLowerCase().startsWith("ar") ? "ar" : "en";
}

export function I18nProvider({ children }) {
	const [lang, setLang] = useState(() => resolveInitialLang());

	useEffect(() => {
		if (typeof document !== "undefined") {
			document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
			document.documentElement.lang = lang;
		}
		if (typeof window !== "undefined") {
			window.localStorage.setItem("lang", lang);
		}
	}, [lang]);

	function getByPath(obj, path) {
		if (!obj || !path) return undefined;
		return path.split(".").reduce((acc, key) => (acc && acc[key] !== undefined ? acc[key] : undefined), obj);
	}

	const t = useMemo(() => {
		return (key, fallback) => {
			const dict = translations[lang] || {};
			// First check if the key exists directly at the root level (not nested)
			if (dict[key] !== undefined && dict[key] !== null && typeof dict[key] !== "object") {
				return dict[key];
			}
			// If not found at root level, try nested path
			const val = getByPath(dict, key);
			// Ensure we return a string/number, not an object
			if (val !== undefined && val !== null && typeof val !== "object") {
				return val;
			}
			// If val is an object or undefined, use fallback or key
			return fallback ?? key;
		};
	}, [lang]);

	const value = { lang, setLang, t };
	return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
	return useContext(I18nContext);
}


